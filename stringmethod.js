// Sample string
var sampleString = "   Hello, World!   ";

// String length
console.log("Length:", sampleString.length);

// String slice()
console.log("Slice:", sampleString.slice(2, 7));

// String substring()
console.log("Substring:", sampleString.substring(2, 7));


// String replace()
console.log("Replace:", sampleString.replace("World", "Universe"));

// String toUpperCase()
console.log("UpperCase:", sampleString.toUpperCase());

// String toLowerCase()
console.log("LowerCase:", sampleString.toLowerCase());

// String concat()
var anotherString = " Have a nice day!";
console.log("Concatenation:", sampleString.concat(anotherString));

// String trim()
console.log("Trim:", sampleString.trim());

